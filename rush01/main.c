#include <stdio.h>

int main(int argc, char **argv)
{
	int k;

	k = 0;
while (k < 9)
{
	int i;
	int j;
	int nums[3][3] = {{1, 2, 3}, {4, 5, 6}, {7, 8, 9}};

	i = 0;
	j = 0;
	while (i < 3)
	{
		while (j < 3)
		{
			printf("%d  ",nums[i][j]);
			j++;
		}
		j = 0;
		i++;
		printf("\n");
	}
	printf("\n");
	k++;
}

	return (0);
}
