void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int sign;
	int tag;

	tag = 0;
	sign = 1;
	if (nb < 0)
	{
		ft_putchar('-');
		if (nb == -2147483648)
		{
			ft_putchar('2');
			ft_putnbr(nb % 1000000000 * -1);
		}
		else
			ft_putnbr(nb *= -1);
	}
	else if (nb < 10)
	{
		ft_putchar(nb + '0');
	}
	else
	{
		ft_putnbr(nb / 10);
		ft_putchar(nb % 10 + '0');
	}
}
