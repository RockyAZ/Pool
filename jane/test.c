#include <unistd.h>

int main(int argc, char **v)
{
	int i;

	i = 0;
	while(v[1][i] != '\0')
	{
		while (v[1][i] == ' ' || v[1][i] == '\t')
			i++;
		while (v[1][i] != ' ' && v[1][i] != '\t')
		{
			write(1, &v[1][i], 1);
			i++;
		}
		write(1, "   ", 3);
		i++;
	}
//	argv[1][i] = '\0';
	write(1, "\n", 1);
	//return (0);
}
