int	ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int j;
	int res;

	i = 0;
	j = 0;
	res = 0;
	while (tab[i] != 0)
	{
		while (tab[i][j] != '\0')
		{
			if (f(tab[i][j]) == 1)
				res++;
			j++;
		}
		j = 0;
		i++;
	}
	return (res);
}
