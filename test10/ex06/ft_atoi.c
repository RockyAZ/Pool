#include <stdio.h>

int	ft_atoi(char *str)
{
	int i;
	int res;
	int min;

	i = 0;
	res = 0;
	min = 1;
	while (str[i] < 33)
		i++;
	if (str[i] == '-' || str[i] == '+')
		i++;
	if (str[i - 1] == '-')
		min *= -1;
	while (str[i] != '\0' && (((str[i] - '0') >= 0) && (str[i] - '0') < 10))
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	return (res * min);
}
